#define WIDTH 1280
#define HEIGHT 720
void fontdata(int,int,double,double,int[],int);
int main(void)
{
    int win;
    int aiueo[]={0x3042,0x3044,0x3046};//Unicodeで入力 https://ja.wikipedia.org/wiki/Unicode%E4%B8%80%E8%A6%A7_0000-0FFF
    int lp=sizeof(aiueo)/sizeof(aiueo[0]);
    win=gopen(WIDTH,HEIGHT);                    /* ウィンドゥ番号を格納する変数*/
    newpen(win,3);
    fontdata(win,64,300.0,300.0,aiueo,lp);
    fontdata(win,32,300.0,400.0,aiueo,lp);
    fontdata(win,16,300,500,aiueo,lp);
    ggetch();
    gclose(win); /* グラフィックス用ウィンドゥを閉じる*/
    return 0;    /* 終了*/
}
